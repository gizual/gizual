export type FileIcon = {
  icon: string;
  color: [string | null, string | null];
};

export function getFileIcon(kind: number): FileIcon | undefined {
  if (kind < 0) throw new Error("Invalid file.kind");

  const entry = FILE_ICON_DB[kind];

  if (!entry) {
    return undefined;
  }
  return {
    icon: entry[0],
    color: entry[1],
  };
}

